package com.programes.logicalquestion1;

public class Teat3 {

    public static void leftshift(int i, int j) 
    {
        i<<=j; 
    } 
    public static void main(String args[]) 
    {
        int i = 4, j = 2; 
        leftshift(i, j); 
        System.out.println(i); 
    } 
}
