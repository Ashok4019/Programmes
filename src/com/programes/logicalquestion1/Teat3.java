package com.programes.logicalquestion1;

public class Teat3 {

    public static void leftshift(int i, int j) 
    {
        System.out.println(i<<=j); 
    } 
    public static void main(String args[]) 
    {
        int i = 4, j = 2;                      //32 16 8 4 2 1
        leftshift(i, j);                    //0  0 0 1 0 0
		                                     
                                                  
    } 
}
